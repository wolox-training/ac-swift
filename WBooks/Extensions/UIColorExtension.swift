import Foundation
import UIKit

extension UIColor {    
    static func polar() -> UIColor {
        return UIColor(red: 0.92, green: 0.96, blue: 0.98, alpha: 1)
    }
    
    static func monza() -> UIColor {
        return UIColor(red: 0.82, green: 0.01, blue: 0.11, alpha: 1)
    }
    
    static func cerulean() -> UIColor {
        return UIColor(red: 0, green: 0.68, blue: 0.93, alpha: 1)
    }
    
    static func atlantis() -> UIColor {
        return UIColor(red: 0.65, green: 0.8, blue: 0.22, alpha: 1)
    }
}
