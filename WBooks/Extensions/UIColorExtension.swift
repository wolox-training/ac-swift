import Foundation
import UIKit

extension UIColor {
    
    static func fontDarkGray() -> UIColor {
        return .darkGray
    }
    
    static func bgLightBlue() -> UIColor {
        return UIColor(red: 0.92, green: 0.96, blue: 0.98, alpha: 1)
    }
}
